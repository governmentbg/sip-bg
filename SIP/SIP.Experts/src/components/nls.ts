type nlsType = typeof en & typeof bg;

let nlss = {
    bg: bg,
    en: en
} as { [key: string]: nlsType }

export type projectTypes = { nlsl: nlsType, nlsu: nlsType, nls: nlsType, locale: string }

export let nls = {
    nlsl: {},
    nlsu: {},
    nls: {}
} as projectTypes & nlsTypes

export const extendNls = (baseNls: nlsTypes) => {
    for (let key in nlss[baseNls.langKey]) {
        nls.nlsl[key] = { ...nlss[baseNls.langKey][key] };
        nls.nlsu[key] = { ...nlss[baseNls.langKey][key] };
        nls.nls[key] = nlss[baseNls.langKey][key];
    }
    // createCases(nls.nlsl, false);
    // createCases(nls.nlsu, true);
    nls.nlsl = { ...baseNls.nlsl, ...nls.nlsl };
    nls.nlsu = { ...baseNls.nlsu, ...nls.nlsu };
    nls.nls = { ...baseNls.nls, ...nls.nls };
}